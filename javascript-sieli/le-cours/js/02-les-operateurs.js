// Rappel, je peux déclarer plusieurs variables sur
// une seule ligne (à la suite, séparé par une ",").
let nb1, nb2, resultat;
nb1 = 10;
nb2 = 5;

// ## L' Addition
resultat = nb1 + nb2;
console.log(resultat);

// ## La soustraction
resultat = nb1 - nb2;
console.log(resultat);

// ## La multiplication
resultat = nb1 * nb2;
console.log(resultat);

// ## La division
resultat = nb1 / nb2;
console.log(resultat);

// ## Le modulo
resultat = nb1 % nb2;
console.log(resultat);
